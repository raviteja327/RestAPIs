    public function create(Request $request){

        $valid = Validator::make($request->all() , [
            'menu_name' => 'required | unique:App\Models\API\menu,menu_name',
            'c_hash' => 'required',
            'c_token' => 'required',
            'c_sec_key' => 'required',
        ]);
 
        if($valid->fails() == TRUE){
            return response()->json(array(
                'status' => 0,
                'message' => $valid->errors()
            ));
        }
        else{

            $c_hash = $request->c_hash;
            $c_token = $request->c_token;
            $c_sec_key = $request->c_sec_key;

            $status = companies::where('c_hash', $c_hash)->where('c_token', $c_token)->where('c_sec_key', $c_sec_key)->where('status', 1)->get();

            if ($status) {
                
                $menu = new menu;

                $menu->menu_name = $request->menu_name;
                $menu->menu_hash = md5($request->menu_name.now());
                $menu->page_name = $request->page_name;
                $menu->c_hash = $request->c_hash;
                $menu->c_token = $request->c_token;
                $menu->c_sec_key = $request->c_sec_key;
                $menu->created_by = "NULL";
                $menu->updated_by = "NULL";
                $menu->created_at = now();
                $menu->updated_at = now();

                $menus = $menu->save();

                if ($menus) {
                    return response()->json(array(
                        'status' => 1,
                        'message' => $menu
                    ));
                } else {
                    return response()->json(array(
                        'status' => 0,
                        'message' => 'Not Saved'
                    ));
                }

            } else {
                return response()->json(array(
                    'status' => 0,
                    'message' => 'Invalid access details Saved'
                ));
            }
            
        }

    }

